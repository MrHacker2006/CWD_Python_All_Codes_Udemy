'''This is a NEW LINE esc'''
print("1.Hello\nBhidu")
print("\n")

'''This is a HORIZONTAL TAB esc'''
print("2.Hello\tBhidu \n")

'''This is a VERTICAL TAB esc'''
print("3.Hello\vBhidu \n")

'''This is a BACKSLASH TAB esc'''
print("4.Hello\\Hi \n")

'''This is SINGLE QUOTE esc'''
print("5.\'Hello\' \n")

'''This is a DOUBLE QUOTE esc'''
print("6.\"Hello\" \n")

'''This is a BELL esc'''
print("7.Alert\a  \n")

'''This is a BACKSPACE esc'''
print("8.Hello\bWorld")
