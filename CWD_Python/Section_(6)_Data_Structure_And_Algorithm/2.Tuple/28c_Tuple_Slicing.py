t1 = ("Gautam", "Saurabh", "Vikram", "Sarthak")
#        0          1         2          3       

print(t1[0:4]) # It will print the full tuple
print(t1[:2])  # It will print the element from starting to the (given index-1)
print(t1[2:])  # It will start print from the number of the first index till the end of the tuple
print("\n")

print(t1[0:4:2]) # It will print the element by leaving each one consecutive element 