# In sets , you can't print the element using index because they are unordered collection
# You can repeat element inside an set
s1 = {1,2,3,4,90}
s2 = {3,2,3,4,90}
s3 = {'Gautam',2,3,4,90}

print(s1)
print("\n")

print(s2) # As you can see it is printing the repeated element only single time, not multiple time
print("\n")

print(s3) # It is printing the set in the random order each time when you run